					<p class="date">
						Concarneau. Un exercice de grande ampleur dans l’emprise portuaire 
					</p>
					<p>
						Un important exercice simulant un incendie à bord d’une vedette transportant des passagers s’est déroulé, vendredi 13 avril, dans l’emprise portuaire à Concarneau.
						
						Il est 20h00, le commandant de l’AZENOR lance un appel de détresse et signale à son bord un début d’incendie alors que le navire entre dans le port. Parmi les nombreux passagers, le navire transporte aussi des enfants scolarisés sur la commune, partis en excursion aux îles Les Glénan. Dans un mouvement de panique, certains occupants se jettent à l’eau.
						Tel est le scénario imaginé pour cet exercice organisé par le groupement territorial de Concarneau. Le choix d’une thématique en milieu portuaire n’est pas le fruit du hasard : la ville accueillait, une semaine plus tard, le départ de la transat AG2R-La Mondiale.
						
						Par ailleurs, l’exercice constituait aussi une préparation à la saison estivale et touristique, propice aux excursions et activités nautiques dans notre département.
						
						L’exercice grandeur nature a mobilisé 94 sapeurs-pompiers issus de 19 centres d’incendie et de secours ainsi que la SNSM, la gendarmerie maritime, la capitainerie du port de pêche, la ville de Concarneau, la Chambre de Commerce et d’Industrie Métropolitaine Bretagne Ouest (CCIMBO) et la compagnie Croisières Bleues, propriétaire de l’AZENOR.
						
						Pour donner une dimension plus réaliste à l’exercice, 47 Jeunes Sapeurs-Pompiers (JSP) des 3 sections du groupement territorial de Concarneau ont joué, pour l’occasion, le rôle des victimes.
						
						L’exercice avait pour objectif principal de décliner en interne les dispositions du plan ORSEC-SNV : Organisation de la Réponse de la SEcurité Civile – Secours à Nombreuses Victimes.
						
						L’exercice répondait également à quatre autres niveaux d’objectifs :
						<ul>
							<li>S’entraîner en inter services en prenant en compte les règles d’intervention spécifiques à ce plan de secours.</li>
							<li>Mettre en œuvre le nouveau Véhicule de Poste de Commandement (VPC) de Bannalec et le Poste Médical Avancé (PMA) de Quimper.</li>
							<li>Tester le dispositif SINUS qui permet de suivre les victimes en temps réel par l’apposition d’un bracelet doté d’un code barre et d’une fiche médicale de l’avant.</li>
							<li>Mettre en œuvre la chaîne médicale du groupement Santé impliquant l’intervention des médecins et infirmiers sapeurs-pompiers.</li>
						</ul>
						« L’exercice a suscité une forte mobilisation parmi les personnels du SDIS 29 » a souligné le Lieutenant-colonel Cédric BOUSSIN, chef du groupement territorial de Concarneau. « Il a permis de mettre en œuvre les différents volets du dispositif ORSEC-SNV. La coopération entre services en termes d’organisation et de communication a été efficace ».
					</p>
